    private void btnImprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnImprimirActionPerformed
        String path = "";
        JFileChooser j = new JFileChooser();
        j.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        int x = j.showSaveDialog(this);

        if (x == JFileChooser.APPROVE_OPTION) {
            path = j.getSelectedFile().getPath();
        }

        Document document = new Document();
        try {
            PdfWriter.getInstance(document, new FileOutputStream("Reporte.pdf"));

            document.open();
            PdfPTable tbl = new PdfPTable(7);

            //AÃ±adir un header
            tbl.addCell("Numero Orden");
            tbl.addCell("Cliente");
            tbl.addCell("Fecha");
            tbl.addCell("Producto");
            tbl.addCell("Precio Unitario");
            tbl.addCell("Cantidad");
            tbl.addCell("Precio Total");

            for (int i = 0; i < tblReportes.getRowCount(); i++) {
                String ID = tblReportes.getValueAt(i, 0).toString();
                String Cliente = tblReportes.getValueAt(i, 1).toString();
                String Fecha = tblReportes.getValueAt(i, 2).toString();
                String Producto = tblReportes.getValueAt(i, 3).toString();
                String PrecioUnitario = tblReportes.getValueAt(i, 4).toString();
                String Cantidad = tblReportes.getValueAt(i, 5).toString();
                String PrecioTotal = tblReportes.getValueAt(i, 6).toString();

                tbl.addCell(ID);
                tbl.addCell(Cliente);
                tbl.addCell(Fecha);
                tbl.addCell(Producto);
                tbl.addCell(PrecioUnitario);
                tbl.addCell(Cantidad);
                tbl.addCell(PrecioTotal);

            }

            document.add(tbl);

        } catch (FileNotFoundException ex) {
            Logger.getLogger(frmReportes.class.getName()).log(Level.SEVERE, null, ex);
        } catch (DocumentException ex) {
            Logger.getLogger(frmReportes.class.getName()).log(Level.SEVERE, null, ex);
        }

        document.close();
    }//GEN-LAST:event_btnImprimirActionPerformed

